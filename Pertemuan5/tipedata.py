# Mengenal berbagai tipe data
print("=== MENGENAL TIPE DATA ===")

# String (teks)
nama = "Budi Santoso"
alamat = "Jalan Merdeka No. 123"
print("Nama (string):", nama)
print("Alamat (string):", alamat)
print("Tipe data nama:", type(nama))

# Integer (angka bulat)
umur = 14
jumlah_saudara = 2
print("Umur (integer):", umur)
print("Jumlah saudara (integer):", jumlah_saudara)
print("Tipe data umur:", type(umur))

# Float (angka desimal)
tinggi_badan = 155.5
berat_badan = 45.2
print("Tinggi badan (float):", tinggi_badan)
print("Berat badan (float):", berat_badan)
print("Tipe data tinggi:", type(tinggi_badan))

# Boolean (benar/salah)
suka_matematika = True
suka_olahraga = False
print("Suka matematika (boolean):", suka_matematika)
print("Suka olahraga (boolean):", suka_olahraga)
print("Tipe data suka_matematika:", type(suka_matematika))